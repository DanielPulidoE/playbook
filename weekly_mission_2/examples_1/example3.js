// Ejemplo 3: forEach para imprimir los países en letras mayúsculas
const countries = ['Finland', 'Denmark', 'Sweden', 'Norway', 'Iceland']
console.log("Ejemplo 5: Imprimiendo la lista de países en mayúsculas")
countries.forEach((element) => console.log(element.toUpperCase()))
/* La sintaxis es:
cada elemento de la lista 'countries' será un parámetro 'element'
la función flecha imprimirá en consola cada parámetro con MAYUSCULAS */